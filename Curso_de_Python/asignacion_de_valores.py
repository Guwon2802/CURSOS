#ASIGNACION DE VALORES NUMERICOS

#Una variable almacena un dato.
numero = 10

#Se puede imprimir el dato de la variable.
print(numero)
print(type(numero))

numero = 10.56

print(numero)
#Con la funcion "type" muestra el tipo de dato que almacena.
print(type(numero))


