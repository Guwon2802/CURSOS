# SUMA

resultado = 10 + 5
print(resultado)

# Se pueden poner los valores directamente.


# RESTA

resultado = 10 - 5
print(resultado)


# MULTIPLICAcion

resultado = 5 * 2
print(resultado)


# DIVISION

resultado = 10 / 2
print(resultado)

# Los resultados siempre te los da en decimal(float)

resultado = 10 // 3
print(resultado)

# Con doble "//" te da el resultado entero, redondeado para abajo.


# POTENCIACION

resultado = 2 ** 5
print(resultado)


# MODULO

resultado = 10 % 3
print(resultado)

# Funciona como una division pero te da el resultado sobrante. 


