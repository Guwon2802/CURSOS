'''Niveles de importancia de los operadores(por ahora)

1. Parentesis
2. Potenciacion
3. Multiplicacion, Division y Modulo
4. Suma y Resta

'''
#SUMA

#Se pueden poner los valores directamente.
resultado = 10 + 5
print(resultado)


#RESTA

resultado = 10 - 5
print(resultado)


#MULTIPLICAcion

resultado = 5 * 2
print(resultado)


#DIVISION

#Los resultados siempre te los da en decimal(float)
resultado = 10 / 2
print(resultado)

#Con doble "//" te da el resultado entero, redondeado para abajo.
resultado = 10 // 3
print(resultado)


#POTENCIACION

resultado = 2 ** 5
print(resultado)


#MODULO

#Funciona como una division, pero te da el resultado sobrante. 
resultado = 10 % 3
print(resultado)