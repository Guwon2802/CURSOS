# Sirve aumentarle valor, o quitarselo, a una variable de forma mas corta.
# Primero se debe crear la variable.

a = 0

a += 5   # Suma en asignacion
a -= 2   # Resta en asigacion
a *= 3   # Multiplicacion en asignacion
a /= 3   # Division en asignacion
a **= 2  # Potenciacion en asignacion
a %= 2   # Moludo en asignacion

print(a)