'''
Compara 2 valores entre si y produce un resultado de True o False.
Tienen el mismo nivel de prioridad. Si hay muchos en una ecuacion, los resuelve de izquierda a derecha.
Los operadores relacionales tiene menor prioridad que los aritmeticos.
'''

#MAYOR QUE

resultado = 3 > 10
print(resultado)


#MENOR QUE

resultado = 5 > 10
print(resultado)


#MAYOR O IGUAL QUE

resultado = 1 >= 3
print(resultado)


#MENOR O IGUAL QUE

resultado = 2 <= 1
print(resultado)


#DIFERENCIAL

resultado = 6 != 6
print(resultado)


#IGUALDAD

resultado = 8 == 9
print(resultado)