'''
Determinamos cuantas veces se repetira el bucle.
'''

for i in range(50): #Crea una coleccion ficticia. La recorre desde el 0 hasta un numero menos.
    print(i)

for i in range(5,11): #Primer valor: Desde donde va a comenzar.
    print(i)          #Segundo valor: Hasta donde va a llegar(+1).

for i in range(2,21,2): #Tercer valor: Desde cuanto en cuanto va a aumentar.
    print(i)

for i in range(20,1,-2): #Lo mismo, pero en decreciente.
    print(i)