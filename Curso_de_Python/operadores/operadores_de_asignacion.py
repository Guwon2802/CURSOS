'''
Sirve para aumentarle valor, o quitarselo, a una variable de forma mas corta.
'''

a = 0 #Variable
print("Valor original:", a)

a += 5   #Suma en asignacion
print("Suma en asignacion:", a)

a -= 2   #Resta en asigacion
print("Resta en asignacion:", a)

a *= 3   #Multiplicacion en asignacion
print("Multiplicacion en asignacion:", a)

a /= 3   #Division en asignacion
print("Division en asignacion:", a)

a **= 2  #Potenciacion en asignacion
print("Potenciacion en asignacion:", a)

a %= 2   #Moludo en asignacion
print("Moludo en asignacion:", a)